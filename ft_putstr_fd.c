#include "ft_printf.h"

int	ft_putstr_fd(char *s)
{
	int	i;

	if (s == NULL)
	{
		i = write(1, "(null)", 6);
		return (i);
	}
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
